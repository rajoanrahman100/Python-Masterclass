# Multiline Strings
a='''This is ihan,
He is a naughty boy,
Alwasy smiles when he talks.
'''
print(a)

#Length of String

print(len(a))

# Strings are arrays
print(a[0]) # Will print the first value of the String, which is 'T'

#Looping through string

y="This is my son, ihan"

for a in y:
    print(a)

# Check a String
# To check if a certain phrase or character is present in a string, we can use the keyword in.

print("ihan" in y)    

